package sec01.ex02;

public class MultiStringVarTest {
    public static void main(String[] args) {
        String name, address, major; // 문자열 변수 동시 선언

        name = "홍길동";
        address = "서울시 강동구";
        major = "컴퓨터 공학과";

        System.out.println("이름: " + name);
        System.out.println("주소: " + address);
        System.out.println("전공: " + major);
    }
}
